/**
 * config-schema.ts — Whitelist of editable env keys exposed in the Glance
 * Settings panel, grouped by category, with type/default/description metadata.
 *
 * Anything NOT in this schema is invisible to the UI (cannot be read or written).
 * Adding a key here is an opt-in to web-UI editability.
 */

export type FieldType =
  | "enum"
  | "string"
  | "path"
  | "boolean"
  | "csv"
  | "secret";

export interface ConfigField {
  key: string;
  type: FieldType;
  label: string;
  description: string;
  default?: string;
  options?: string[];      // for enum
  placeholder?: string;
  sensitive?: boolean;     // mask value in GET, never include in plaintext
  recommendedScope?: "project" | "global";
  requiresRestart?: boolean; // changes require a Glance restart to fully apply
}

export interface ConfigGroup {
  id: string;
  label: string;
  description: string;
  fields: ConfigField[];
}

