/** Flatten schema into a Map for O(1) lookup */
export function buildKeyIndex(): Map<string, ConfigField> {
  const m = new Map<string, ConfigField>();
  for (const g of CONFIG_SCHEMA) for (const f of g.fields) m.set(f.key, f);
  return m;
}

export function isEditableKey(key: string): boolean {
  return buildKeyIndex().has(key);
}

export function getField(key: string): ConfigField | undefined {
  return buildKeyIndex().get(key);
}

export function maskSecret(v: string): string {
  if (!v) return "";
  if (v.length <= 6) return "***";
  return v.slice(0, 4) + "***" + v.slice(-2);
}
